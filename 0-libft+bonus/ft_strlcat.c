#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t			i;
	size_t			j;
	size_t			srcln;
	size_t			destln;

	destln = 0;
	while (dest[destln] != '\0' && destln < size)
		destln++;
	srcln = ft_strlen(src);
	if (size <= destln)
		return (size + srcln);
	i = destln;
	j = 0;
	while (src[j] != '\0' && i < size - 1)
	{
		dest[i++] = src[j++];
	}
	dest[i] = '\0';
	return (srcln + destln);
}

