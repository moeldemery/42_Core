#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	srt_index;
	size_t	end_index;
	size_t	i;
	size_t	size;

	i = 0;
	if (!s1)
		return (NULL);
	while (s1[i] && ft_strchr(set, s1[i]))
		i++;
	srt_index = i;
	i = 0;
	size = ft_strlen(s1);
	while (s1[size - i - 1] && ft_strchr(set, s1[size - i - 1]))
		i++;
	end_index = size - i;
	return (ft_substr(s1, srt_index, (end_index - srt_index))); 
}
